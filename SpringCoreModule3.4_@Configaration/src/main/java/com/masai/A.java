package com.masai;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

@Service
public class A {

	
	@Autowired
	private B b;
	
	
	
	
	public void showA() {
		System.out.println("I am inside the A class");
		System.out.println("I am calling the class B show method");
		System.out.println("B Object Address "+b);
		b.showB(); 
	
	}
	
	
	
	
}
